//Escreva um programa Java que calcule a média de três números e exiba o resultado na tela.
public class MediaTresNumeros {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int num3 = 30;
        double media = (num1 + num2 + num3) / 3.0;
        System.out.println("A média de " + num1 + ", " + num2 + " e " + num3 + " é: " + media);
    }
}
